public class VariablesInJava {
  public static void main(String[] args) {
    // Constant is Fixed Values that donot change

    // Variables are store container where we can store the changing values
    // Here int is a integer datatype. In Java there are 8 Primitive Datatypes (byte, short, int, float, long, double, char, boolean) and their sizes are in bytes (1 byte = 8 bits) (1, 1, 4, 4, 8, 8, 2, 1).
    
    int a = 10;
    int b = 25;
    int sum = a + b;
    System.out.println(sum);
    System.out.println(a * b);
  }
}
